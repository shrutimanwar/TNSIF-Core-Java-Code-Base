package Day2;

public class Decision_making_with_operator {

	public static void main(String[] args) {
		
		int x=8, y =7;
		int a=10, b=11;
		
		if(x>=y)
		{
			System.out.println("true ");
		}
		else
		{
			System.out.println("False");
		}
		//   a>b
		if(!(a<b) || (a==b))
		{
			System.out.println("Condition is true ");
		}
		else
		{
			System.out.println("Condition is false");
		}

	}

}
